import cmath

# Define a complex number
z = 3 + 4j

# Calculate cosine of z
print("Cosine of z:", cmath.cos(z))

# Calculate exponential of z
print("Exponential of z:", cmath.exp(z))

# Calculate natural logarithm of z
print("Natural logarithm of z:", cmath.log(z))

# Calculate base-10 logarithm of z
print("Base-10 logarithm of z:", cmath.log10(z))

# Calculate phase of z
print("Phase of z:", cmath.phase(z))

# Get polar form of z
print("Polar form of z:", cmath.polar(z))

# Get complex number with modulus and phase
print("Complex number with modulus and phase:", cmath.rect(5, cmath.pi / 3))

# Calculate sine of z
print("Sine of z:", cmath.sin(z))

# Calculate square-root of z
print("Square-root of z:", cmath.sqrt(z))

# Constants
print("Value of e:", cmath.e)
print("Value of pi:", cmath.pi)
