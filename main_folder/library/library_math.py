import math

# Trigonometric functions
x = 0.5
print("acos(x):", math.acos(x))
print("acosh(x):", math.acosh(x + 1))
print("asin(x):", math.asin(x))
print("asinh(x):", math.asinh(x))
print("atan(x):", math.atan(x))
print("atan2(y, x):", math.atan2(1, 2))
print("atanh(x):", math.atanh(x))
print("cos(x):", math.cos(x))
print("cosh(x):", math.cosh(x))
print("sin(x):", math.sin(x))
print("sinh(x):", math.sinh(x))
print("tan(x):", math.tan(x))
print("tanh(x):", math.tanh(x))

# Logarithmic and exponential functions
print("exp(x):", math.exp(x))
print("expm1(x):", math.expm1(x))
print("log(x):", math.log(x))
print("log10(x):", math.log10(x))
print("log2(x):", math.log2(x))

# Power and root functions
print("pow(x, 2):", math.pow(x, 2))
print("sqrt(x):", math.sqrt(x))

# Rounding and truncation
print("ceil(x):", math.ceil(x))
print("floor(x):", math.floor(x))
print("trunc(x):", math.trunc(x))

# Other mathematical functions
print("copysign(x, -1):", math.copysign(x, -1))
print("degrees(pi):", math.degrees(math.pi))
print("radians(180):", math.radians(180))
print("fabs(-5):", math.fabs(-5))
print("gamma(x):", math.gamma(x))
print("lgamma(x):", math.lgamma(x))
print("erf(x):", math.erf(x))
print("erfc(x):", math.erfc(x))
print("fmod(x, 3):", math.fmod(x, 3))
print("modf(x):", math.modf(x))
print("ldexp(x, 2):", math.ldexp(x, 2))

# Checking for special values
print("isfinite(5):", math.isfinite(5))
print("isinf(5):", math.isinf(5))
print("isnan(5):", math.isnan(5))

# Constants
print("Value of e:", math.e)
print("Value of pi:", math.pi)
